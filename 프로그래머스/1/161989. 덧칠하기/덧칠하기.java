class Solution {
    public int solution(int n, int m, int[] section) {
        int answer = 0; //  롤러로 페인트칠한 횟수
        int painted = 0; // 페인트칠이 끝난 가장 오른쪽 구역의 다음 위치
        
        for (int start : section) {
            if (painted <= start) {
                painted = start + m;
                answer++;
            }
        }
        
        return answer;
    }
}

