/*
📝 코드 동작 원리 및 설명
	1.	변수 초기화
•	answer: 롤러를 최소 몇 번 사용해야 하는지 카운트하는 변수입니다.
•	painted: 롤러를 한 번 굴렸을 때, 페인트가 칠해진 범위의 바로 다음 구역 번호를 기록합니다. (즉, 이 번호보다 작은 구역은 이미 칠해졌다는 뜻입니다.)
	2.	배열 순회 (Greedy 방식)
•	section 배열은 오름차순으로 주어지므로, 왼쪽(작은 번호)부터 차례대로 탐색합니다.
•	for (int start : section)을 통해 칠해야 할 구역 번호를 하나씩 꺼냅니다.
	3.	페인트칠 여부 확인 및 갱신
•	if (painted <= start): 현재 구역(start)이 이전에 칠했던 페인트 범위(painted)를 벗어났는지 확인합니다.
•	벗어났다면 아직 페인트가 칠해지지 않은 곳이므로 롤러를 사용해야 합니다.
•	start 위치에서 롤러를 굴리면 start부터 start + m - 1까지 칠해집니다. 따라서 다음번에 칠을 시작해야 할 최소 위치를 start + m으로 갱신(painted = start + m)하고 카운트(answer++)를 증가시킵니다.

🚀 시간 복잡도
•	section 배열의 크기만큼만 단 한 번 순회하므로 시간 복잡도는 **O(N)**입니다. (N은 section의 길이)
•	주어진 범위에서 성능 저하 없이 가장 빠르고 효율적으로 동작합니다.
*/
